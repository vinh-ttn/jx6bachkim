	end
	local IsClone = server_getdata(tbGIFT.szFile,"GIFT_CODE_CHECK",nVar);
	local nillVar = 0
	if IsClone ~= "" then 
		return Say("M gift ny  c s dng,nu cha c hy lin h gm thng qua facebook  nhn!!")
	end
